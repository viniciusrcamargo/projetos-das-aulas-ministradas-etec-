var numero1 = 25;
var numero2 = 35;

function soma(num1, num2){
    return num1 + num2
}

function subtracao(num1, num2){
    return num1 - num2
}

function multiplicacao(num1, num2){
    return num1 * num2
}

function divisao(num1, num2){
    return num1 / num2
}

console.log("A soma dos números é: " + soma(numero1, numero2))
console.log("A subtração dos números é: " + subtracao(numero1, numero2))
console.log("A múltiplicação dos números é: " + multiplicacao(numero1, numero2))
console.log("A divisão dos números é: " + divisao(numero1, numero2))